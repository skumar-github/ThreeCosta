function makeLine(scene, ptA, ptB){

    
    var material = new THREE.LineBasicMaterial({
        color: 0x0000ff,
    });   	


	var geometry = new THREE.Geometry();
    geometry.vertices.push(ptA);
    geometry.vertices.push(ptB);
    
    var line = new THREE.Line(geometry, material);
    scene.add(line);
	

 }
   

